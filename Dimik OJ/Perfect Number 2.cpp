#include <iostream>

using namespace std;

int main()
{
    int t;
    cin >> t;

    while(t--)
    {
        long long int n;
        cin >> n;

            if(n >= 6)
                cout << 6 << endl;

            if(n >= 28)
                cout << 28 << endl;

            if(n >= 496)
                cout << 496 << endl;

            if(n >= 8128)
                cout << 8128 << endl;

            if(n >= 33550336)
                cout << 33550336 << endl;


        if(t != 0);
            cout << endl;
    }
    return  0;
}
